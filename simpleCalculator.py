# ⁡⁣⁢⁣𝗦𝗶𝗺𝗽𝗹𝗲 𝗰𝗮𝗹𝗰𝘂𝗹𝗮𝘁𝗼𝗿⁡

a = int(input("Enter first number:"))
b = int(input("Enter second number:"))
operator = str(input("Enter an operator:"))

if operator == "+":
    print(a+b)
elif operator == "-":
    print(a-b)
elif operator == "*":
    print(a*b)
elif operator == "/":
    print(a/b)
elif operator == "**":
    print(a**b)
else:
    print("Invalid operator")
 
